//operadores vão nos permitir fazer operações matemáticas, de comporação e lógica.

// Aritméticos
console.log(2+2)
console.log(2-2)
console.log(2*2)
console.log(8/4)

// De comparação

// Igual (==) - Retorna verdadeiro se os valores comparados forem iguais.

console.log(2 == 2)
console.log(2 == 3)

// Não igual (!=) - Retorna verdadeiro se os valores comparados não forem iguais.
// Esse operador também pode ser chamado de diferente de.

console.log(2 != 2)
console.log(2 != 1)

// Igual estrito (===) - Só retorna verdadeiro se o valor e o tipo comparados forem iguais.

console.log(2 === "2")

// Não igual estrito (!==)- Esse operador vai retornar verdadeiro se os valores e ou os tipos forem diferentes

console.log(3 !== "3")
console.log("3" !== "3")

// Maior que (>)- Compara se o operador da esquerda é maior que o da direita. Caso seja retorna verdadeiro
console.log(2 > 3)
console.log(2 > 1)

// Maior ou igual que (>=) - Compara se o operador da esquerda é menor que o da direita. 
// Caso seja retorna verdadeiro.

console.log(2 >= 3)
console.log(2 >= 2)
console.log(2 >= 1)

// Menor que (<) - Compara se o operador da esquerda é maior ou igual ao da direita. 
// Caso seja retorna verdadeiro.

console.log(2 < 3)
console.log(2 < 1)

// Menor ou igual que (<=) - Compara se o operador da esquerda é menor ou igual ao da direita. 
//  Caso seja retorna verdadeiro.

console.log(2 <= 3)
console.log(2 <= 2)
console.log(2 <= 1)